package calculator


class Calculator {

    fun add (num1 : Int, num2 : Int): Int {
        return num1 + num2
    }

    fun times (num1: Int, num2 : Int) : Int {
        return num1 * num2
    }

    fun divide (num1: Int, num2 : Int) : Int {
        return num1 / num2
    }

    fun subtract (num1: Int, num2 : Int) : Int {
        return num1 - num2
    }


}